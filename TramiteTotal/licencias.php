    <p><strong>Obtención por primera vez:</strong></p>
    <p>Los requisitos generales son:
        <ul>
            <li>Certificado de educación básica.</li>
            <li>Cédula de identidad.</li>
            <li>18 años de edad cumplidos (excepcionalmente, se puede optar con 17).</li>
            <li>Examen teórico-práctico.</li>
            <li>Examen médico psicotécnico y de la vista.</li>
        </ul>
        <p><strong>Importante</strong>: revise los requisitos según <a href="http://mejoresconductores.conaset.cl/" target="_blank" rel="noopener">tipo de licencia</a>.</p>
    </p>
    <p><strong>Vigencia:</strong></p>
    <p>
        <ul>
            <li>Licencias no profesionales: seis años.</li>
            <li>Licencias profesionales: cuatro años.</li>
            <li>Licencias especiales: seis años.</li>
        </ul>
        <p>Los plazos pueden ser restringidos por causas especiales, como el uso de lentes y otros.</p>
    </p>
