                    <li><strong><a href="https://mejoresconductores.conaset.cl/#/LicenciaClaseC" target="_blank" rel="noopener">Clase C</a></strong> y <strong><a href="https://mejoresconductores.conaset.cl/#/LicenciaClaseCR" target="_blank" rel="noopener">clase CR</a></strong>: para vehículos con motor fijo o agregado (motocicletas, motonetas).</li>
